package Mosaics;

import java.util.ArrayList;

public class DynamicAttributes {
	
	Object option;
	ArrayList<DAttribute> attributes;
	
	
	public Object getOption() {
		return option;
	}
	public void setOption(Object option) {
		this.option = option;
	}
	public ArrayList<DAttribute> getAttributes() {
		return attributes;
	}
	public void setAttributes(ArrayList<DAttribute> attributes) {
		this.attributes = attributes;
	}
	
	
	
	

}
